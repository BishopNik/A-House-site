let activeProject = 0;

function renderProject(index, animate = true) {
	activeProject = (index + 4) % 4;
	const p = projectData[language][activeProject];
	const wrap = document.querySelector('.project-image-wrap');
	if (animate) wrap.classList.add('is-changing');
	setTimeout(
		() => {
			const img = document.querySelector('#project-image');
			img.src = p.image;
			img.alt = p.alt;
			document.querySelector('#project-image-num').textContent = String(
				activeProject + 1
			).padStart(2, '0');
			document.querySelector('#project-type').textContent = p.type;
			document.querySelector('#project-title').innerHTML = p.title;
			document.querySelector('#project-description').textContent = p.description;
			document.querySelector('#project-format').textContent = p.format;
			document.querySelector('#project-floors').textContent = p.floors;
			document.querySelector('#project-plan').textContent = p.plan;
			document.querySelector('#project-site').textContent = p.site;
			document
				.querySelectorAll('.project-tabs button')
				.forEach((b, i) => b.classList.toggle('active', i === activeProject));
			wrap.classList.remove('is-changing');
		},
		animate ? 220 : 0
	);
}

