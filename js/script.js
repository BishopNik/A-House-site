let ticking = false;
function updateScroll() {
	const max = document.documentElement.scrollHeight - innerHeight;
	document.querySelector('.scroll-progress').style.width = `${max ? (scrollY / max) * 100 : 0}%`;
	document.querySelector('.header').classList.toggle('scrolled', scrollY > 20);
	document.querySelectorAll('.parallax-media img').forEach(img => {
		const rect = img.parentElement.getBoundingClientRect();
		if (rect.bottom > 0 && rect.top < innerHeight)
			img.style.transform = `translateY(${(rect.top - innerHeight / 2) * -0.025}px) scale(1.045)`;
	});
	ticking = false;
}
addEventListener(
	'scroll',
	() => {
		if (!ticking) {
			requestAnimationFrame(updateScroll);
			ticking = true;
		}
	},
	{ passive: true }
);

